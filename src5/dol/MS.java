package dol;

public class MS {
	
	Double x;
	int Suma;
	
	public MS(double x, int suma) {
		super();
		this.x = x;
		Suma = suma;
	}
	
	public Double getX() {
		return x;
	}

	public void setX(Double x) {
		this.x = x;
	}

	public int getSuma() {
		return Suma;
	}

	public void setSuma(int suma) {
		Suma = suma;
	}

	
	

}
